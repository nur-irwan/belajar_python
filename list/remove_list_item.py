#gunakan method remove() untuk menghapus item pada list

#remove banana
thisList = ["banana","orange","mango","papaya"]
thisList.remove("banana")
print(thisList)

#gunakan method pop()untuk menghapus list dengan nilai index
thisList = ["banana","orange","mango","papaya"]
thisList.pop(1)
print(thisList)

