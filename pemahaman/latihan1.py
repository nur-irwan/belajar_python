#latihan konversi dan perhitungan sederhana
#Input dari user
#ubah ke integer
#Operasi matematika dasar

#input user
angka1 = input("Masukkan Angka pertama : ")
angka2 = input("Masukkan Angka kedua :")
#ubah ke integer
angka1 = int(angka1)
angka2 = int(angka2)
#perhitungan sederhana
tambah = angka1 + angka2
bagi = angka1 / angka2
kurang = angka1 - angka2
kali = angka1 * angka2

print("Hasil tambah adalah :", tambah)
print("Hasil bagi adalah :", bagi)
print("Hasil kurang adalah :", kurang)
print("Hasil kali adalah :", kali)

