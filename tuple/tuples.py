#tuples adalah salah satu tipe data pada python selain list, set dan dictionary
#tuple adalah koleksi yg diurutkan dan tidak dapat diubah
#tuple dibuat dengan tanda kurung ()

thisTuple = ("apple", "banana","cherry")
print(thisTuple)

#tuple dapat berisi value yg sama
thisTuple = ("apple","banana","apple","cherry")
print(thisTuple)

#untuk mengetahui berapa banyak item pada tuples, gunakan function len()
thisTuple = ("apple","banana", 30)
print(len(thisTuple))